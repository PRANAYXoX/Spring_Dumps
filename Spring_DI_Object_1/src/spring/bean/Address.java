package spring.bean;

public class Address {
	
	private String city;
	private String state;
	private long pin;
	
	
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public long  getPin() {
		return pin;
	}
	public void setPin(int pin) {
		this.pin = pin;
	}
}
